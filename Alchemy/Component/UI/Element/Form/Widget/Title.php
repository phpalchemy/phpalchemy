class Title extends Widget
{
    protected $text = "";

    public function __construct(array $attributes = array())
    {
        parent::__construct($attributes);
        $this->setXtype('title');
    }

    /**
     * @param string $text
     */
    public function setText($text)
    {
        $this->text = $text;
    }

    /**
     * @return string
     */
    public function getText()
    {
        return $this->text;
    }
}

